public class datatypes {
    public static void main(String[] args)
    {
        int a=100;// int datatype
        //char datatype
        char c='a';
        //string datatype
        String ch="hello";
        //boolean datatype
        boolean b=true;
        //float datatype
        float f= 5.99F;
        System.out.println(a);

        System.out.println(c);

        System.out.println(ch);

        System.out.println(b);

        System.out.print(f);

    }
}
